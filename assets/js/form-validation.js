// Dinamic dropdown based by chosen area

function createOption(ddl, text, value) {
  var opt = document.createElement("option");
  opt.value = value;
  opt.text = text;
  ddl.options.add(opt);
}

function createOptions(optionsArray, ddl) {
  for (i = 0; i < optionsArray.length; i++) {
    createOption(ddl, optionsArray[i], optionsArray[i]);
  }
}

function getComuni() {
  var p = document.getElementById("mce-PROVINCIA");
  var c = document.getElementById("mce-COMUNE");

  c.options.length = 0;

  var provincia = p.options[p.selectedIndex].value;
  switch (provincia) {
    case "Belluno":
      createOptions(provBelluno, c);
      break;
    case "Padova":
      createOptions(provPadova, c);
      break;
    case "Rovigo":
      createOptions(provRovigo, c);
      break;
    case "Treviso":
      createOptions(provTreviso, c);
      break;
    case "Venezia":
      createOptions(provVenezia, c);
      break;
    case "Verona":
      createOptions(provVerona, c);
      break;
    case "Vicenza":
      createOptions(provVicenza, c);
      break;
  }
}
